package com.example.entity;


/**
 * 门类信息
*/
public class Categorys {
    /** 主键ID */
    private Integer id;
    /** 门类名称 */
    private String name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}